    private void CadastraEnderecoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastraEnderecoActionPerformed
        Integer idCliente_Endereco = Integer.parseInt(this.id_endereco.getText());
        Integer cep = Integer.parseInt(this.cep_cliente.getText());
        Endereco endereco = new Endereco(idCliente_Endereco,
            this.rua_cliente.getText(),
            this.numero_endereco_cliente.getText(),this.bairro_cliente.getText(),
            this.cidade_cliente.getText(),this.uf_cliente.getText(), this.complemento.getText(),
            cep);
        this.enderecos.add(endereco);
        JOptionPane.showMessageDialog(this, "Endereço cadastrado com sucesso!");
        LimparEndereco();
    }//GEN-LAST:event_CadastraEnderecoActionPerformed

