    private void CadastrarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastrarClienteActionPerformed
        Integer idCliente = Integer.parseInt(this.codigo_cliente.getText());
        Integer ddd = Integer.parseInt(this.ddd_cliente.getText());
        Integer numero = Integer.parseInt(this.numero_cliente.getText());
        Cliente cliente = new Cliente(idCliente, this.nome_cliente.getText(),
                this.email_cliente.getText(),this.cpf_cliente.getText(),ddd,numero );
        cliente.setEnderecos(this.enderecos);
        Repositorio.clientes.add(cliente);
        System.out.println(cliente);
       JOptionPane.showMessageDialog(this, "Cliente cadastrado com sucesso!");
       limparCliente();
    }//GEN-LAST:event_CadastrarClienteActionPerformed

