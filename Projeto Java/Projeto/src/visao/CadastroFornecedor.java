    private void CadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CadastrarActionPerformed
        Integer id = Integer.parseInt(this.jTextField11.getText());
        String nome = this.jTextField1.getText();
        String cnpj = this.jTextField2.getText();
        Integer contato= Integer.parseInt(this.jTextField3.getText());
        String email= this.jTextField4.getText();
       
        Fornecedor fornecedor = new Fornecedor(id, nome, cnpj, email, contato);    
        fornecedor.setEnderecos(this.enderecos);
        Repositorio.fornecedores.add(fornecedor);
        System.out.println(fornecedor);
        
        JOptionPane.showMessageDialog(this, "Fornecedor cadastrado com sucesso!");
        limpar();   
    }//GEN-LAST:event_CadastrarActionPerformed

