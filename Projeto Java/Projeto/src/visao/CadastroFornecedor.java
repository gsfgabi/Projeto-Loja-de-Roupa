    private void AdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AdicionarActionPerformed
        Integer id = Integer.parseInt(this.jTextField12.getText());
        Integer cep = Integer.parseInt(this.jTextField6.getText());
        String rua=this.jTextField7.getText();
        String numero = this.jTextField5.getText();
        String complemento =this.jTextField8.getText();
        String bairro=this.jTextField9.getText();
        String cidade=this.jTextField10.getText();
        String uf= (String) this.jComboBox1.getSelectedItem();
        
        Endereco e1 = new Endereco (id,rua, numero, bairro, cidade, uf, complemento, cep);
        this.enderecos.add(e1); 
        System.out.println(e1);
        JOptionPane.showMessageDialog(this, "Endereco cadastrado com sucesso!");
        this.jTextField12.setText("");
        this.jTextField6.setText("");
        this.jTextField10.setText("");
        this.jTextField5.setText("");
        this.jTextField9.setText("");
        this.jTextField7.setText("");
        this.jTextField8.setText("");
        this.jComboBox1.setSelectedIndex(0);
        this.jTextField12.requestFocus();
    }//GEN-LAST:event_AdicionarActionPerformed
    
